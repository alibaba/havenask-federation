package org.havenask.engine.stop.searcher.action;

import org.havenask.action.ActionResponse;
import org.havenask.common.io.stream.StreamInput;
import org.havenask.common.io.stream.StreamOutput;

import java.io.IOException;

public class SearcherStopResponse extends ActionResponse {
    private final String result;
    private final int resultCode;

    public SearcherStopResponse(StreamInput in) throws IOException {
        result = in.readString();
        resultCode = in.readInt();
    }

    public SearcherStopResponse(String result, int resultCode) {
        this.result = result;
        this.resultCode = resultCode;
    }

    public String getResult() {
        return result;
    }

    public int getResultCode() {
        return resultCode;
    }

    @Override
    public void writeTo(StreamOutput out) throws IOException {
        out.writeString(result);
        out.writeInt(resultCode);
    }
}
